package ovs.graph.node;

import imgui.type.ImString;
import ovs.graph.Graph;
import ovs.graph.PinData;
import ovs.graph.pin.PinVar;

public class NodeCurrentArrayElement extends Node {

    PinVar output = new PinVar();

    public NodeCurrentArrayElement(Graph graph) {
        super(graph);
        setName("Current Array Element");

        output.setNode(this);
        addCustomOutput(output);
    }

    @Override
    public void execute() {
        PinData<ImString> outputData = output.getData();

        outputData.getValue().set("Current Array Element");
    }

    @Override
    public String getOutput() {
        PinData<ImString> outputData = output.getData();
        return outputData.getValue().get();
    }

    @Override
    public void UI() {

    }

    @Override
    public String getTooltip() {
        return "The current array element being considered. only meaningful during the evaluation of values such as filtered array and sorted array.";
    }
}