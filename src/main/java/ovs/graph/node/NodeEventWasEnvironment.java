package ovs.graph.node;

import imgui.type.ImString;
import ovs.graph.Graph;
import ovs.graph.PinData;
import ovs.graph.pin.PinVar;

public class NodeEventWasEnvironment extends Node {

    PinVar output = new PinVar();

    public NodeEventWasEnvironment(Graph graph) {
        super(graph);
        setName("Event Was Environment");

        output.setNode(this);
        addCustomOutput(output);
    }

    @Override
    public void execute() {
        PinData<ImString> outputData = output.getData();

        outputData.getValue().set("Event Was Environment");
    }

    @Override
    public String getOutput() {
        PinData<ImString> outputData = output.getData();
        return outputData.getValue().get();
    }

    @Override
    public void UI() {

    }

    @Override
    public String getTooltip() {
        return "Whether the elimination was due to the environment for the event currently being processed by this rule";
    }
}