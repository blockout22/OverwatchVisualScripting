package ovs.graph.node;

import imgui.type.ImString;
import ovs.graph.Graph;
import ovs.graph.PinData;
import ovs.graph.pin.PinAction;

public class NodeEnableBuiltInGameModeMusic extends Node{

    PinAction output = new PinAction();

    public NodeEnableBuiltInGameModeMusic(Graph graph) {
        super(graph);
        setName("Enable Built In Game Mode Music");

        output.setNode(this);
        addCustomOutput(output);
    }

    @Override
    public void execute() {
        PinData<ImString> outputData = output.getData();

        outputData.getValue().set("Enable Built-In Game Mode Music;");

    }

    @Override
    public String getOutput() {
        PinData<ImString> outputData = output.getData();
        return outputData.getValue().get();
    }

    @Override
    public void UI() {

    }
}
