package ovs;

public class RGBA {

    public float r = 1f;
    public float g = 1f;
    public float b = 1f;
    public float a = 1f;

    public RGBA(float r, float g, float b, float a) {
    }

    public void set(float r, float g, float b, float a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }
}
